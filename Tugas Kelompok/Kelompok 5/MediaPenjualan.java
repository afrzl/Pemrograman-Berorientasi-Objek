public class MediaPenjualan {
  private String email;
  private String marketPlace;
  private String pesanInstan;
  private String mediaSosial;

  public String getEmail() {
    return email;
  }

  public String getMediaSosial() {
    return mediaSosial;
  }

  public String getMarketPlace() {
    return marketPlace;
  }

  public String getPesanInstan() {
    return pesanInstan;
  }

  public void setEmail(String pesanInstan) {
    this.pesanInstan = pesanInstan;
  }

  public void setMediaSosial(String mediaSosial) {
    this.mediaSosial = mediaSosial;
  }

  public void setPesanInstan(String pesanInstan) {
    this.pesanInstan = pesanInstan;
  }

  public void setMarketPlace(String marketPlace) {
    this.marketPlace = marketPlace;
  }
}
