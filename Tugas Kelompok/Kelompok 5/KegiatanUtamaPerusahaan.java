public class KegiatanUtamaPerusahaan {
  private String kegiatanUtamaPerusahaan;
  private String kodeKatUsaha;
  private int kbliDigit;

  public KegiatanUtamaPerusahaan() {}

  public String getKegiatanUtamaPerusahaan() {
    return kegiatanUtamaPerusahaan;
  }

  public void setKegiatanUtamaPerusahaan(String kegiatanUtamaPerusahaan) {
    this.kegiatanUtamaPerusahaan = kegiatanUtamaPerusahaan;
  }

  public String getKodeKatUsaha() {
    return kodeKatUsaha;
  }

  public void setKodeKatUsaha(String kodeKatUsaha) {
    this.kodeKatUsaha = kodeKatUsaha;
  }

  public int getKbliDigit() {
    return kbliDigit;
  }

  public void setKbliDigit(int kbliDigit) {
    this.kbliDigit = kbliDigit;
  }
}
